function calcula() {


    /**
     * Formula de resta por medio de los inputs
     */
    var a = Number(document.getElementById('carga1').value);
    var b = Number(document.getElementById('indi1').value);
    var c = a - b;
    document.getElementById('error1').value = c;


    /**
     * Obtengo los valores de los inputs
     */
    document.getElementById("carga9").value = document.getElementById("carga1").value;
    document.getElementById("indi9").value = document.getElementById("indi1").value;
    document.getElementById("error9").value = document.getElementById("error1").value;

}



